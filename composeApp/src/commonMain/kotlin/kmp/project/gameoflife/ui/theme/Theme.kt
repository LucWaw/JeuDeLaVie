@Composable
fun DragAndDropTheme(darkTheme: Boolean = isSystemInDarkTheme(), content: @Composable () -> Unit) {
    MaterialTheme(
        colorScheme = platformColors(darkTheme),
        content = content,
        shapes = Shapes,
        typography = Typography,
    )
}